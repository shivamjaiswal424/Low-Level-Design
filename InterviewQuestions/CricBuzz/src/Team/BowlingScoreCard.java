package Team;

public class BowlingScoreCard {
    public int totalOverDeliver;
    public int runsGiven;
    public int wicketTaken;
    public int noBallCount;
    public int wideBallCount;
    public double economyRate;

    public int getTotalOverDeliver() {
        return totalOverDeliver;
    }

    public void setTotalOverDeliver(int totalOverDeliver) {
        this.totalOverDeliver = totalOverDeliver;
    }

    public int getRunsGiven() {
        return runsGiven;
    }

    public void setRunsGiven(int runsGiven) {
        this.runsGiven = runsGiven;
    }

    public int getWicketTaken() {
        return wicketTaken;
    }

    public void setWicketTaken(int wicketTaken) {
        this.wicketTaken = wicketTaken;
    }

    public int getNoBallCount() {
        return noBallCount;
    }

    public void setNoBallCount(int noBallCount) {
        this.noBallCount = noBallCount;
    }

    public int getWideBallCount() {
        return wideBallCount;
    }

    public void setWideBallCount(int wideBallCount) {
        this.wideBallCount = wideBallCount;
    }

    public double getEconomyRate() {
        return economyRate;
    }

    public void setEconomyRate(double economyRate) {
        this.economyRate = economyRate;
    }
}
